#!/usr/bin/env python3
"""
cost_model.py — AIOps Platform Break-Even Calculator (W3-D3)

Implements the is_worth_it() function per §8.3 specification:
  monthly_value = incidents_per_month × avg_incident_duration_hours
                  × expected_mttr_reduction_pct × downtime_cost_per_hour
  roi = monthly_value / aiops_monthly_cost
  verdict: roi > 1.5 → worth_it | 1.0 < roi ≤ 1.5 → marginal | roi ≤ 1.0 → not_worth_it
"""

from __future__ import annotations


def is_worth_it(
    num_services: int,
    incidents_per_month: int,
    avg_incident_duration_hours: float,
    downtime_cost_per_hour: float,
    expected_mttr_reduction_pct: float = 0.4,
    aiops_monthly_cost: float = 15_000,
) -> dict:
    """
    Calculate whether deploying an AIOps platform is economically justified.

    Parameters
    ----------
    num_services : int
        Number of services in the monitored stack. Context metric — higher
        service count generally means more incident blast radius.
    incidents_per_month : int
        Average number of production incidents per month.
    avg_incident_duration_hours : float
        Average duration of each incident in hours (MTTR before AIOps).
    downtime_cost_per_hour : float
        Business cost in USD per hour of downtime. Includes direct revenue
        loss, SLA penalties, on-call opportunity cost, and customer trust.
    expected_mttr_reduction_pct : float
        Expected fraction of MTTR that AIOps eliminates (default: 0.4 = 40%).
        Sourced from industry benchmarks: AIOps reduces MTTR by 30–50% through
        faster detection, automated RCA, and runbook suggestions.
    aiops_monthly_cost : float
        Total monthly cost of the AIOps platform in USD. Includes compute,
        storage, model inference, licensing, and allocated engineer time.

    Returns
    -------
    dict with keys:
        monthly_value   : float  — USD value generated by AIOps per month
        monthly_cost    : float  — USD cost of AIOps per month
        roi             : float  — monthly_value / monthly_cost
        payback_months  : float  — months until cumulative value breaks even with cost
                                   (float('inf') if monthly_value == 0)
        verdict         : str    — 'worth_it' | 'marginal' | 'not_worth_it'
    """
    # Value: total downtime hours per month × fraction eliminated × cost per hour
    monthly_downtime_hours: float = incidents_per_month * avg_incident_duration_hours
    monthly_value: float = (
        monthly_downtime_hours
        * expected_mttr_reduction_pct
        * downtime_cost_per_hour
    )

    roi: float = monthly_value / aiops_monthly_cost if aiops_monthly_cost > 0 else float("inf")

    payback_months: float = (
        aiops_monthly_cost / monthly_value if monthly_value > 0 else float("inf")
    )

    if roi > 1.5:
        verdict = "worth_it"
    elif roi > 1.0:
        verdict = "marginal"
    else:
        verdict = "not_worth_it"

    return {
        "monthly_value": round(monthly_value, 2),
        "monthly_cost": round(aiops_monthly_cost, 2),
        "roi": round(roi, 4),
        "payback_months": round(payback_months, 2) if payback_months != float("inf") else float("inf"),
        "verdict": verdict,
    }


def _print_scenario(label: str, result: dict) -> None:
    """Pretty-print a scenario result."""
    bar = "─" * 55
    print(f"\n{bar}")
    print(f"  {label}")
    print(bar)
    print(f"  Monthly value generated : ${result['monthly_value']:>10,.2f}")
    print(f"  Monthly AIOps cost      : ${result['monthly_cost']:>10,.2f}")
    print(f"  ROI ratio               : {result['roi']:>10.4f}x")
    payback = result["payback_months"]
    payback_str = f"{payback:.2f} months" if payback != float("inf") else "∞ (never)"
    print(f"  Payback period          : {payback_str:>15}")
    print(f"  Verdict                 : {result['verdict'].upper()}")
    print(bar)


if __name__ == "__main__":
    # ─────────────────────────────────────────────────────────────────────
    # Scenario 1 — Small startup, few incidents
    #   20 services, 2 incidents/month × 1h, $10k/h downtime, $15k AIOps
    #   Expected ROI: 0.53 → not_worth_it (matches §8.4 example)
    # ─────────────────────────────────────────────────────────────────────
    s1 = is_worth_it(
        num_services=20,
        incidents_per_month=2,
        avg_incident_duration_hours=1,
        downtime_cost_per_hour=10_000,
        aiops_monthly_cost=15_000,
    )
    _print_scenario(
        "Scenario 1 — Small startup (20 svcs, 2 inc/mo × 1h, $10k/h, $15k AIOps)",
        s1,
    )
    print("  Analysis: Only $8,000/mo value vs $15,000 cost. Too few incidents to justify")
    print("  the overhead. Recommendation: invest in good observability + on-call culture")
    print("  first. Revisit when incident count > 5/month or downtime cost > $15k/h.")

    # ─────────────────────────────────────────────────────────────────────
    # Scenario 2 — Mid-size e-commerce, real scale
    #   100 services, 5 incidents/month × 2h, $20k/h downtime, $25k AIOps
    #   Expected ROI: 3.2 → worth_it (matches §8.4 example)
    # ─────────────────────────────────────────────────────────────────────
    s2 = is_worth_it(
        num_services=100,
        incidents_per_month=5,
        avg_incident_duration_hours=2,
        downtime_cost_per_hour=20_000,
        aiops_monthly_cost=25_000,
    )
    _print_scenario(
        "Scenario 2 — Mid-size e-commerce (100 svcs, 5 inc/mo × 2h, $20k/h, $25k AIOps)",
        s2,
    )
    print("  Analysis: $80,000/mo value vs $25,000 cost. ROI 3.2x — clearly justified.")
    print("  At this scale, AIOps pays for itself in 0.31 months (~9 days) of operation.")
    print("  The 40% MTTR reduction saves 4h of downtime per month, each hour worth $20k.")

    # ─────────────────────────────────────────────────────────────────────
    # Scenario 3 — Payment processing fintech (custom scenario)
    #   Industry: Payment gateway / fintech processing card transactions
    #   Stack: 80 microservices (auth, fraud-detection, settlement,
    #          reconciliation, wallet, gateway, notification, etc.)
    #   Incidents: 4/month (mix: latency spikes, DB connection exhaustion,
